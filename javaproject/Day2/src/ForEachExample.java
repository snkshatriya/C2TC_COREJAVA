public class ForEachExample {
	    public static void main(String[] args) 
	    {
	        int[] numbers = {1, 2, 3, 4, 5};
	        
	        // Iterate through each element in the array using the for-each loop
	        for (int number : numbers)
	        {
	            System.out.print(number + " ");
	        }
	    }
}



